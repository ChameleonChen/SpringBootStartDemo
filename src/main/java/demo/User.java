package demo;

public class User {
	
	private String userId;
	
	private String userName;
	
	public String getUserId() {
		return userId;
	}
	
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	public void setUserName (String userName) {
		this.userName = userName;
	}
	
	public String getUserName() {
		return userName;
	}
}
